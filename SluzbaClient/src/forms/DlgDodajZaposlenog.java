    private void srediCB() {
        cbKatedra.removeAllItems();
        cbStatus.removeAllItems();
        cbStatus.addItem("Redovan profesor");
        cbStatus.addItem("Vanredan profesor");
        cbStatus.addItem("Asistent");
        cbStatus.addItem("Administrator");
        Request req=new Request();
        req.setOperation(Operations.VRATIKATEDRE);
        Communication.getInstance().sendRequest(req);
    }

    public void popuniCB(ArrayList<Katedra> parametar) {
        for(Katedra k:parametar){
            cbKatedra.addItem(k);
        }
    }

    public void dodajZaposlenog(boolean uspesno) {
        if(uspesno){
            JOptionPane.showMessageDialog(this, "Uspesno!");
            ClientController.getInstance().setDdz(null);
            this.dispose();
        }else{
            JOptionPane.showMessageDialog(this, "Greska!");
        }
    }
    


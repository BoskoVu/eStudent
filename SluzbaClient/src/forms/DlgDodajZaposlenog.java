    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        SimpleDateFormat sdf=new SimpleDateFormat("dd.MM.yyyy.");
        String ime=txtIme.getText();
        String prezime=txtPrezime.getText();
        String username=txtUsername.getText();
        String password=txtPassword.getText();
        String status=(String) cbStatus.getSelectedItem();
        Katedra katedra;
        if(status.equals("Administrator")){
            katedra=new Katedra(0, "");
        }else{
            katedra=(Katedra) cbKatedra.getSelectedItem();
        }
        try {
            Date datum=sdf.parse(txtDatumRodjenja.getText());
            if(ime.isBlank()||prezime.isBlank()||username.isBlank()||password.isBlank()){
                JOptionPane.showMessageDialog(this, "Unesite sve podatke");
            }else{
                if(username.matches("[a-zA-Z0-9]+@zaposleni.fon.bg.ac.rs")&&password.length()>5){
                    Zaposleni z=new Zaposleni(-1, ime, prezime, username, password, datum, status, katedra);
                    Request req=new Request();
                    req.setParametar(z);
                    req.setOperation(Operations.DODAJZAPOSLENOG);
                    Communication.getInstance().sendRequest(req);
                }else{
                    JOptionPane.showMessageDialog(this, "Username i/ili password nisu u odgovarajucem formatu");
                }
            }
        } catch (ParseException ex) {
            JOptionPane.showMessageDialog(this, "Pogresan format datuma!(dd.MM.yyyy.)");
        }
    }//GEN-LAST:event_jButton1ActionPerformed


    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        SimpleDateFormat sdf=new SimpleDateFormat("dd.MM.yyyy. hh:mm");
        try {
            Date datum=sdf.parse(txtDatum.getText());
            Rok rok=(Rok) cbRok.getSelectedItem();
            Predmet predmet=(Predmet) cbPredmet.getSelectedItem();
            if(datum.after(rok.getPocetak())&&datum.before(rok.getZavrsetak())){
                Termin termin=new Termin(-1, predmet, rok, datum);
                Request req=new Request();
                req.setParametar(termin);
                req.setOperation(Operations.DODAJTERMIN);
                Communication.getInstance().sendRequest(req);
            }else{
                JOptionPane.showMessageDialog(this, "Datum nije u navedenom roku");
            }
        } catch (ParseException ex) {
            JOptionPane.showMessageDialog(this, "Pogresan format datuma(dd.MM.yyyy. hh:mm)");
        }
    }//GEN-LAST:event_jButton2ActionPerformed


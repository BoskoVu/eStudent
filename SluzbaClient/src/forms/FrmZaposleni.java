    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        ModelPolaganjaZaposleni mpz=(ModelPolaganjaZaposleni) tblPolaganja.getModel();
        ArrayList<Polaganje> listaP=mpz.getLista();
        boolean b=true;
        for(Polaganje pol:listaP){
            if(!(pol.getOcena()==0||(pol.getOcena()>=5&&pol.getOcena()<=10))){
                b=false;
            }
        }
        if(b){
            Request req=new Request();
            req.setParametar(listaP);
            req.setOperation(Operations.UNESIOCENE);
            Communication.getInstance().sendRequest(req);
        }else{
            JOptionPane.showMessageDialog(this, "Pogresan unos ocena");
            srediTabelu();
        }
    }//GEN-LAST:event_jButton1ActionPerformed


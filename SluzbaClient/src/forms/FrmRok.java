    private void srediTabelu() {
        ModelRok mr=new ModelRok();
        tblRok.setModel(mr);
        Request req= new Request();
        req.setOperation(Operations.VRATIODGOVARAJUCETERMINE);
        req.setParametar(ClientController.getInstance().getIzabraniRok());
        Communication.getInstance().sendRequest(req);
    }

    public void postaviTabeluTermina(Object parametar) {
        ArrayList<Termin> listaT=(ArrayList<Termin>) parametar;
        ModelRok mr=(ModelRok) tblRok.getModel();
        mr.setListaT(listaT);
    }

    private void namestiBtn() {
        Date datum=new Date();
        long d=(ClientController.getInstance().getIzabraniRok().getPocetak().getTime()-datum.getTime())/86400000;
        if(d>5&&d<10){
            btnPrijavi.setEnabled(true);
        }else{
            btnPrijavi.setEnabled(false);
        }
    }

    public void prijaviPolaganje(boolean uspesno) {
        if(uspesno){
            JOptionPane.showMessageDialog(rootPane, "Uspesna prijava");
        }else{
            JOptionPane.showMessageDialog(rootPane, "Neuspesna prijava");
        }
    }

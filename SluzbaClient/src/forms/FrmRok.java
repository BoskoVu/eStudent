    private void btnPrijaviActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnPrijaviActionPerformed
        int i=tblRok.getSelectedRow();
        if(i==-1){
            JOptionPane.showMessageDialog(rootPane, "Izaberite termin");
        }else{
            ModelRok mr=(ModelRok) tblRok.getModel();
            Termin t=mr.getListaT().get(i);
            Request req=new Request();
            Polaganje p=new Polaganje(ClientController.getInstance().getPrijavljeniStudent(), t, 0, 0);
            req.setParametar(p);
            req.setOperation(Operations.PRIJAVIPOLAGANJE);
            Communication.getInstance().sendRequest(req);
        }
    }//GEN-LAST:event_btnPrijaviActionPerformed


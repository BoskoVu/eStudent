    private void srediCB() {
        cbKatedra.removeAllItems();
        for(Katedra k:ClientController.getInstance().getListaKatedra()){
            cbKatedra.addItem(k);
        }
        txtNaziv.setText(ClientController.getInstance().getIzmeniPredmet().getNaziv());
        txtESPB.setText(ClientController.getInstance().getIzmeniPredmet().getESPB()+"");
    }

    public void izmeniPredmet(boolean uspesno) {
        if(uspesno){
            JOptionPane.showMessageDialog(this, "Uspesno!");
            ClientController.getInstance().setDip(null);
            ClientController.getInstance().getFa().srediCB();
            ClientController.getInstance().getFa().unosPredmeta();
            this.dispose();
        }else{
            JOptionPane.showMessageDialog(this, "Greska!");
        }
    }

    private void btnIzmeniActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnIzmeniActionPerformed
        String nazivPredmeta=txtNaziv.getText();
        String ESPB=txtESPB.getText();
        Katedra katedra=(Katedra) cbKatedra.getSelectedItem();
        if(ESPB.isBlank()||nazivPredmeta.isBlank()){
            JOptionPane.showMessageDialog(this, "Unesite sve podatke");
        }else{
            int e;
            try{
                e=Integer.parseInt(ESPB);
                if(e<2||e>12){
                    throw new Exception();
                }
                Predmet p=new Predmet(ClientController.getInstance().getIzmeniPredmet().getPredmetID(), e, nazivPredmeta, katedra);
                Request req=new Request();
                req.setParametar(p);
                req.setOperation(Operations.IZMENIPREDMET);
                Communication.getInstance().sendRequest(req);
            }catch(Exception ex){
               JOptionPane.showMessageDialog(this, "Bodovi moraju biti broj od 2 do 12");
            }
        }
    }//GEN-LAST:event_btnIzmeniActionPerformed

